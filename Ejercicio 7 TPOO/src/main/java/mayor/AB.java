        String Resultado1;
        int valorA,valorB;
        try{
            String datvalorA =String.valueOf(txtValora.getText());
            String datvalorB =String.valueOf(txtValorb.getText());
                    if (datvalorA.isEmpty()||datvalorB.isEmpty()){
                       throw new Exception("Existen campos vacios");
                   }                        
        valorA = Integer.parseInt(txtValora.getText());
        valorB = Integer.parseInt(txtValorb.getText());
        Resultado1=Operaciones.calcularSoluciones(valorA, valorB);
        txtResultado1.setText(String.valueOf(Resultado1));
        }
        catch(NumberFormatException nfe){
            lblMensaje1.setText("Ocurri√≥ un problema con el tipo de dato ingresado");
            txtValora.setText("");
            txtValorb.setText("");
            txtResultado1.setText("");
            
        
        } catch (Exception ex) {
            lblMensaje2.setText("Existen campos vacios");
            lblMensaje1.setText("");
            
        }
